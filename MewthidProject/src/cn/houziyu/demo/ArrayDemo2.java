package cn.houziyu.demo;

public class ArrayDemo2 {
    /*
    数组的取值与赋值
     */
    public static void main(String[] args) {
        int[] arr = {1, 2 ,3};

        // 取arr数组的第一个值
        System.out.println(arr[0]);

        // 把第三个值改为20；
        arr[2] = 20;
        System.out.println(arr[2]);
    }
}
